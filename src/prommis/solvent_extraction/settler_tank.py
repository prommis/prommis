@declare_process_block_class("SettlerTank")
class SettlerTankData(UnitModelBlockData):
    default_initializer = SettlerTankInitializer
    default_scaler = SettlerTankScaler

    CONFIG = ConfigDict(implicit=False)

    CONFIG.declare(
        "has_holdup",
        ConfigValue(default=True, domain=In([True]), doc="Holdup must be included."),
    )
    CONFIG.declare(
        "dynamic",
        ConfigValue(
            default=False,
            domain=DefaultBool,
            doc="Whether model is dynamic (True) or steady state (False).",
        ),
    )
    CONFIG.declare(
        "light_phase_alias",
        ConfigValue(
            default="light",
            domain=str,
            doc="Alias to use for light phase in settler",
        ),
    )

    CONFIG.declare(
        "heavy_phase_alias",
        ConfigValue(
            default="heavy",
            domain=str,
            doc="Alias to use for heavy phase in settler",
        ),
    )

    CONFIG.declare(
        "light_phase_config",
        Stream_Config(
            description="ConfigDict for light phase",
        ),
    )

    CONFIG.declare(
        "heavy_phase_config",
        Stream_Config(
            description="ConfigDict for heavy phase",
        ),
    )

    CONFIG.declare(
        "transformation_method",
        ConfigValue(
            default=useDefault,
            description="Discretization method to use for the length domain",
        ),
    )

    CONFIG.declare(
        "transformation_scheme",
        ConfigValue(
            default=useDefault,
            description="Discretization scheme to use for the length domain",
        ),
    )

    CONFIG.declare(
        "finite_elements",
        ConfigValue(
            default=5,
            domain=int,
            description="Number of finite elements length domain",
            doc="""Number of finite elements to use when discretizing the length
            domain (default=5)""",
        ),
    )

    CONFIG.declare(
        "collocation_points",
        ConfigValue(
            default=3,
            domain=int,
            description="Number of collocation points per finite element",
            doc="""Number of collocation points to use per finite element when
            discretizing length domain (default=3)""",
        ),
    )

    def build(self):
        super().build()

        # Use light phase units for flowsheet-level variables
        self._units = (
            self.config.light_phase_config.property_package.get_metadata().get_derived_units
        )

        for stream_name in ["light_phase", "heavy_phase"]:
            stream_config = getattr(self.config, f"{stream_name}_config")

            # Create control volume
            control_volume = ExtendedControlVolume1DBlock(
                has_holdup=self.config.has_holdup,
                dynamic=self.config.dynamic,
                property_package=stream_config.property_package,
                property_package_args=stream_config.property_package_args,
                transformation_method=self.config.transformation_method,
                transformation_scheme=self.config.transformation_scheme,
                finite_elements=self.config.finite_elements,
                collocation_points=self.config.collocation_points,
                area_definition=DistributedVars.variant,
            )
            self.add_component(stream_name, control_volume)

            # Add geometry and material, energy, and pressure balance equations
            control_volume.add_geometry(
                flow_direction=FlowDirection.forward,
            )
            control_volume.add_state_blocks(
                information_flow=FlowDirection.forward, has_phase_equilibrium=False
            )
            control_volume.add_material_balances(
                balance_type=MaterialBalanceType.componentTotal,
                has_phase_equilibrium=False,
                has_mass_transfer=False,
            )
            control_volume.add_energy_balances(
                balance_type=stream_config.energy_balance_type
            )
            if stream_config.has_pressure_balance:
                control_volume.add_momentum_balance(
                    balance_type=MomentumBalanceType.momentumTotal
                )
            # Discretize length domain
            control_volume.apply_transformation()

            # Add user-specified alias for stream
            stream_alias = getattr(self.config, f"{stream_name}_alias")
            if stream_name == stream_alias:
                # Either using light_phase as the alias
                # for the light phase or heavy_phase
                # as the alias for the heavy phase
                pass

            alias_phase = f"{stream_alias}_phase"
            if hasattr(self, alias_phase):
                raise ConfigurationError(
                    f"Cannot use {alias_phase} as an alias for {stream_name}_phase. "
                    f"An object with that name already exists on {self.name}."
                )
            else:
                add_object_reference(self, alias_phase, control_volume)

            self.add_inlet_port(name=f"{stream_alias}_inlet", block=control_volume)
            self.add_outlet_port(name=f"{stream_alias}_outlet", block=control_volume)

        self._make_geometry()
        self._make_performance()

    def _make_geometry(self):
        add_object_reference(
            self, local_name="length", remote_object=self.light_phase.length
        )

        @self.Constraint(doc="Equate length of phase control volumes")
        def length_eqn(self):
            return self.light_phase.length == pyunits.convert(
                self.heavy_phase.length, to_units=self._units("length")
            )

        self.settler_width = Var(
            initialize=1,
            bounds=(0, None),
            units=self._units("length"),
            doc="Width of main settler tank",
        )
        self.light_phase_weir_channel_width = Var(
            initialize=1,
            bounds=(0, None),
            units=self._units("length"),
            doc="Width of light phase weir channel.",
        )
        self.light_phase_weir_height = Var(
            initialize=1,
            bounds=(0, None),
            units=self._units("length"),
            doc="Height of light phase weir.",
        )
        self.light_phase_height = Var(
            self.flowsheet().time,
            initialize=1,
            bounds=(0, None),
            units=self._units("length"),
            doc="Height of light phase in tank",
        )
        self.heavy_phase_height = Var(
            self.flowsheet().time,
            initialize=1,
            bounds=(0, None),
            units=self._units("length"),
            doc="Height of heavy phase in tank",
        )

        @self.Expression(self.flowsheet().time)
        def total_liquid_height(b, t):
            return b.light_phase_height[t] + b.heavy_phase_height[t]

        @self.Constraint(self.flowsheet().time, self.light_phase.length_domain)
        def light_phase_area_eqn(b, t, x):
            return b.light_phase.area[t, x] == b.light_phase_height[t] * b.settler_width

        @self.Constraint(self.flowsheet().time, self.heavy_phase.length_domain)
        def heavy_phase_area_eqn(b, t, x):
            return (
                pyunits.convert(b.heavy_phase.area[t, x], to_units=self._units("area"))
                == b.heavy_phase_height[t] * b.settler_width
            )

    def _make_performance(self):
        # Assume that the light phase exits the settler tank by
        # flowing over a weir of fixed height. We make no assumption
        # about how the heavy phase exits the tank---the user must
        # define the flow rate themselves, either by fixing it or
        # writing a constraint.

        self.weir_discharge_coefficient = Param(
            # Vanden-Broeck and Keller, Weir flows, J. Fluid Mech., 1987
            initialize=0.583,
            mutable=True,
        )
        self.weir_flow_smoothing_eps = Param(
            # Assuming length units are meters, the
            # height over the top of the weir is usually
            # on the order of millimeters. Therefore, we
            # use a epsilon several orders of magnitude
            # below that.
            initialize=1e-6 * pyunits.m,
            mutable=True,
            units=self._units("length"),
        )

        @self.Constraint(self.flowsheet().time)
        def weir_flow_eqn(b, t):
            xf = b.light_phase.length_domain.last()

            H = smooth_max(
                b.total_liquid_height[t] - b.light_phase_weir_height,
                0 * b._units("length"),
                eps=b.weir_flow_smoothing_eps,
            )
            g = pyunits.convert(
                Constants.acceleration_gravity, to_units=b._units("acceleration")
            )
            # Need to convert the light phase volumetric flowrate because, presently (3/6/26),
            # the property packages of PrOMMiS have volumetric flow rate in L/hr, but the correct
            # derived units are m^3/hr
            return pyunits.convert(
                self.light_phase.properties[t, xf].flow_vol,
                to_units=self._units("flow_vol"),
            ) == pyunits.convert(
                g**0.5
                * H**1.5
                * b.light_phase_weir_channel_width
                * b.weir_discharge_coefficient,
                to_units=self._units("flow_vol"),
            )
