r"""
Settler Tank Model

========================

Author: Douglas Allan

The SettlerTank unit model represents the settler tank portion of the mixer-settler unit operation.
In the settler tank, two phases of different densities separate. The light phase overflows the top
of the tank via a weir. The method by which the heavy tank is unspecified, because there are
multiple mixer-settler configurations in which the heavy phase leaves in different ways (such as
overflowing a second weir, being pumped out of the bottom of the tank, or leaving the bottom of the
tank by the suction generated by the next mixer-settler's impeller.)

Configuration Arguments
-----------------------

- ``has_holdup``: The SettlerTank always has holdup, so this option must equal True.
- ``dynamic``: Boolean determining whether to create a dynamic or steady state model.
- ``light_phase_alias``: String to use as an alias for the light phase. For example, if
    the user has ``light_phase_alias = "organic"``, the ``organic_phase`` attribute will be
    created as a reference to the ``light_phase`` control volume, and the ``organic_inlet``
    and ``organic_outlet`` ports will be created.
- ``heavy_phase_alias``: String to use as an alias for the heavy phase. For example, if
    the user has ``heavy_phase_alias = "aqueous"``, the ``aqueous_phase`` attribute will be
    created as a reference to the ``heavy_phase`` control volume, and the ``aqueous_inlet``
    and ``aqueous_outlet`` ports will be created.
- ``light_phase_config``: Configuration dictionary for the light phase, described below.
- ``heavy_phase_config``: Configuration dictionary for the heavy phase, described below.
- ``transformation_method``: PyomoDAE transformation method for the ``ControlVolume1D`` objects.
- ``transformation_scheme``: PyomoDAE transformation scheme for the ``ControlVolume1D`` objects.
- ``finite_elements``: Number of finite elements to use when discretizing the ``ControlVolume1D``
    objects
- ``collocation_points``: Number of collocation points to use if a collocation method is used
    to discretize the ``ControlVolume1D`` objects.

Stream configurations
---------------------

Both phases are configured using a configuration dictionary.

- ``property_package``: The ``PhysicalParameterBlock``used for the stream's properties.

- ``property_package_args``: Configuration arguments used when creating the stream's ``StateBlock``
    objects.
- ``energy_balance_type``: Which energy balance type, if any, that should be created. The permitted
    options are ``EnergyBalanceType.none``, ``EnergyBalanceType.enthalpyTotal``,
    ``EnergyBalanceType.energyTotal``, and ``EnergyBalanceType.isothermal``. The default value
    is ``EnergyBalanceType.none``.
- ``has_pressure_balance``: Boolean determining whether or not to create pressure balance equations.

Degrees of freedom
------------------
In addition to the variables on the inlet ports, it is expect that the user will specify the
following variables:
- ``length``: The length of the settler tank parallel with the flow
- ``settler_width``: The width of the settler tank perpendicular to the flow
- ``light_phase_weir_channel_width``: The width of the weir through which the light phase drains.
    This is usually the same as the ``settler_width``.
- ``light_phase_weir_height``: The height of the light phase weir measured from the bottom of the
    settler tank.

When the system is at steady state, the user must also specify *exactly* one of the following variables:
- ``light_phase_height``: The height of the light phase measured from the top of the heavy phase.
- ``heavy_phase_height``: The height of the heavy phase measured from the bottom of the settler tank.

Model structure
---------------

In this unit model, the two phases are represented by parallel ControlVolume1D objects. There is
no material transfer between them because it is assumed that all material transfer occurred in the
mixer portion. Each phase takes up a certain height within the tank, and the difference between
the top of the light phase and the light phase weir's height is the driving force for the flow
of the light phase out of the tank. The weir flow relationship is taken from [1]. When the total
liquid height of the tank is less than that of the light phase weir's height, there is zero
flow out of the tank.

Additional Parameters
---------------------

The model has the following parameters:

- ``weir_discharge_coefficient``: The dimensionless discharge coefficient used for weir flow.
    The default value of 0.583 is taken from [1].
- ``weir_flow_smoothing_eps``: A parameter used in ``smooth_max`` to smooth the transition
    between nonzero flow when the liquid height is greater than the tank's height and zero
    flow when the liquid height is less than the tank's height. It has a default value of 1 μm.

Additional Constraints
----------------------

In addition to the constraints generated within the `ControlVolume1D` objects, the unit model
has the following constraints:
- Geometric constraints:
  - ``length_eqn``: A constraint setting the light phase's flow length equal to the heavy
    phase's length.
  - ``light_phase_area_eqn``: A constraint setting the light phase's flow area equal to the
    product of the light phase's height and the settler width.
  - ``heavy_phase_area_eqn``: A constraint setting the heavy phase's flow area equal to the
    product of the heavy phase's height and the settler width.
- Performance Constraint:
  - ``weir_flow_eqn``: A constraint defining the light phase's outlet flow in terms of the
    height of the light phase over the weir.

Additional Expressions
----------------------
The following unit model level expression is created:
- ``total_liquid_height``: The sum of the light phase's and heavy phase's heights.

Bibliography
------------

[1] Vanden-Broeck, J. M., & Keller, J. B. (1987). Weir flows. Journal of fluid mechanics, 176, 283-293.
"""

